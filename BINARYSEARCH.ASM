MYSS SEGMENT PARA STACK 'S'
	DW 20 DUP (?)
MYSS ENDS

MYDS SEGMENT PARA 'D'
DIZI	DB 1, 2, 3, 4, 5, 6, 7, 8, 9
ISLEM   DW 4
ISTENEN DB 5
ORTA	DW ?
SON		DW 8
ILK		DW 0
INDEX DW -1
MYDS ENDS

MYCS SEGMENT PARA 'C'
	ASSUME CS:MYCS, SS:MYSS, DS:MYDS
MAIN PROC FAR
	PUSH DS
	XOR AX, AX
	PUSH AX
	MOV AX, MYDS
	MOV DS, AX
	MOV CX, ISLEM
	CALL NEAR PTR EL
	MOV SI, ORTA
	MOV BL, ISTENEN
L1: CMP BL, DIZI[SI]
    JA L2
	JB L3
    MOV INDEX, SI
    JMP L4
L2: MOV ILK, SI
	CALL NEAR PTR EL    
	LOOP L1
	JMP L4
L3:	MOV SON, SI
	CALL NEAR PTR EL
	LOOP L1
L4: RETF
MAIN ENDP

EL PROC NEAR
	PUSH AX
	PUSH BX
	MOV BL, 2
	MOV AX, ILK
	ADD AX, SON
	DIV BL
	XOR AH, AH
	MOV ORTA, AX
	POP AX
	POP BX
	RET
EL ENDP

MYCS ENDS
	END MAIN

